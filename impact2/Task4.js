//Q4) Declare Two Variables and Perform All Operations

// Arithmetic Operations
console.log("Addition:", a + b);
console.log("Subtraction:", a - b);
console.log("Multiplication:", a * b);
console.log("Division:", a / b);
console.log("Modulus:", a % b);

// Bitwise Operations
console.log("Bitwise AND:", a & b);
console.log("Bitwise OR:", a | b);
console.log("Bitwise XOR:", a ^ b);
console.log("Left Shift:", a << 1);
console.log("Right Shift:", a >> 1);

// Relational Operations
console.log("a > b:", a > b);
console.log("a < b:", a < b);
console.log("a == b:", a == b);

// Assignment Operations
a += b;  // a = a + b
console.log("Updated a (a += b):", a);

// Logical Operations
console.log("Logical AND:", (a > 0) && (b > 0));
console.log("Logical OR:", (a > 10) || (b < 10));
console.log("Logical NOT:", !(a > b));